/*
String: Represents text and is enclosed in single, double, or backticks
(template literals).
Example:
 */
let name = "John";
let message = `Hello, ${name}!`;
//or
let message2 = 'hello' + name;

/*
Boolean: Represents a binary value, true or false, often used for 
conditional statements.
0, null, and undefined are falsey values.
Example: 
*/
let isOnline = true;
let isWorking = false;


/*
Array: A collection of values, indexed by position, and can contain mixed data types.
Example:
*/
let colors = ["red", "green", "blue"];
let mixedArray = [1, "two", true];

/*
Object: Represents a collection of key-value pairs, allowing you to store and access data with named properties.
Example:
*/
let person = {
    name: "Alice",
    age: 25,
    isStudent: true
};


/*
Function: A block of reusable code that can be called with arguments and can return a value.
you can call a function by function()
Example:
 */
function add(a, b) {
    return a + b;
}


/*
Undefined: Represents a variable that has been declared but not assigned a value.
Example:
 */
let x;
console.log(x); // Outputs: undefined


/*
Null: Represents the intentional absence of any object value or no value at all.
Example:
 */
let empty = null;



/*
Symbol: A unique and immutable data type often used as object property keys.
Example: 
*/
const uniqueKey = Symbol('description');



